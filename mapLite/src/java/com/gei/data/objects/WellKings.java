package com.gei.data.objects;

/**
 *
 * @author ileung
 */
public class WellKings {
    
    private Integer x;
    private Integer y;
    private String wellnumber;
    private Integer num;
    private String county;
    private String basin;
    private String subbasin;
    private String bas_sbbsn;

    /**
     * @return the x
     */
    public Integer getX() {
        return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(Integer x) {
        this.x = x;
    }

    /**
     * @return the y
     */
    public Integer getY() {
        return y;
    }

    /**
     * @param y the y to set
     */
    public void setY(Integer y) {
        this.y = y;
    }

    /**
     * @return the wellnumber
     */
    public String getWellnumber() {
        return wellnumber;
    }

    /**
     * @param wellnumber the wellnumber to set
     */
    public void setWellnumber(String wellnumber) {
        this.wellnumber = wellnumber;
    }

    /**
     * @return the num
     */
    public Integer getNum() {
        return num;
    }

    /**
     * @param num the num to set
     */
    public void setNum(Integer num) {
        this.num = num;
    }

    /**
     * @return the county
     */
    public String getCounty() {
        return county;
    }

    /**
     * @param county the county to set
     */
    public void setCounty(String county) {
        this.county = county;
    }

    /**
     * @return the basin
     */
    public String getBasin() {
        return basin;
    }

    /**
     * @param basin the basin to set
     */
    public void setBasin(String basin) {
        this.basin = basin;
    }

    /**
     * @return the subbasin
     */
    public String getSubbasin() {
        return subbasin;
    }

    /**
     * @param subbasin the subbasin to set
     */
    public void setSubbasin(String subbasin) {
        this.subbasin = subbasin;
    }

    /**
     * @return the bas_sbbsn
     */
    public String getBas_sbbsn() {
        return bas_sbbsn;
    }

    /**
     * @param bas_sbbsn the bas_sbbsn to set
     */
    public void setBas_sbbsn(String bas_sbbsn) {
        this.bas_sbbsn = bas_sbbsn;
    }
}
